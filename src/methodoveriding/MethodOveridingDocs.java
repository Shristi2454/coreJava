package methodoveriding;

public class MethodOveridingDocs {

	/*
	 * ======================== Method Overriding =====================
	 * 
	 * # use to achieve polymorphism (method overloading/ overriding)
	 * # to implement method overriding there must be IS-A relationship(inheritance)
	 * # method name. return type and list of arguments same as parent class method but 
	 *   but implementation should be different in child class method
	 *   
	 * # inherit means : child class uses methods and properties of parent class
	 * # override : child class modifies/change/edit parent class methods.
	 * 
	 * e.g. 
	 *    
	 *    class A {
	 *    
	 *         void run(){
	 *                 
	 *               sout(40000;
	 *           
	 *          }
	 *   }
	 *   
	 *    class B extends A {
	 *    
	 *         @Override
	 *          void run(){
	 *             sout(15000);
	 *         }
	 *         
	 *    }
	 *         
	 */
}

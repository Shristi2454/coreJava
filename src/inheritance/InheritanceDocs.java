package inheritance;

public class InheritanceDocs {

	/*
	 * ========================= Inheritance ===============================
	 * # code re-usability
	 * # process of accessing properties and method of parent/super/base class to child/sub/derived class is known as inheritance.
	 * 
	 * # also known as IS-A relationship
	 * # its a generalization to specialization process. 
	 * # we cannot inherit private props. and methods from parent class
	 * # we can inherit static props. and methods
	 * # Constructors are not inherited.
	 * # 'Super' keyword is used to represent parent object.
	 *    --> we can access props., methods  and constructors of parent class to child class through 'Super' keyword 
	 * # we can inherit only one class at a time.
	 * 
	 * 
	 * Syntax: 
	 * 
	 *   class child_class_name extends parents_class_name{
	 *   
	 *        //properties : parent properties + child properties
	 *        
	 *        //methods : parent methods +child methods
	 *   }
	 *   
	 *   e.g.
	 *     
	 *     class A{
	 *     
	 *     
	 *     }
	 *     
	 *     class D{
	 *     
	 *     
	 *     }
	 *     
	 *     class B extends A{
	 *     
	 *     
	 *     }
	 *     
	 */
}
